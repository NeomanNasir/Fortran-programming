PROGRAM amount_year
    WRITE(6,10)
 10 FORMAT(7X,'YEAR',6X,'AMOUNT'//)
    AMOUNT=2000.00
    IYEAR=1997
    WRITE(6,20)IYEAR,AMOUNT
 20 FORMAT(1X,I10,3x,'$'F8.2)
    DO 100 IYEAR=1978,1995
        AMOUNT=AMOUNT+0.06*AMOUNT
        WRITE(6,20)IYEAR,AMOUNT
100 CONTINUE
    STOP
    END
